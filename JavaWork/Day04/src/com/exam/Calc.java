package com.exam;

public class Calc {

	
	public static void Hap(int a, int b) {
		System.out.printf("5d + %5d  = %5d", a, b, (a + b));
	}
	public static void Sub(int a, int b) {
		System.out.printf("5d - %5d  = %5d", a, b, (a - b));
	}
	public static void Mul(int a, int b) {
		System.out.printf("5d * %5d  = %5d", a, b, (a * b));
	
	}
	public static void Div(int a, int b){
		System.out.printf("5d / %5d  = %5d", a, b, (a / b));
	} 
	

}