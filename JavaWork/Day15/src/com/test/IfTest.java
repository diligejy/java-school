package com.test;

public class IfTest {
	public static void main(String[] args) {
		//평균을 기준으로 A, B, C, D, E
		double avg = 98.9;
		if (avg >= 90) {
			System.out.println("A");
		}else if (avg >= 80) {
			System.out.println("B");
		}else if (avg >= 70) {
			System.out.println("C");
		}else if (avg >= 60) {
			System.out.println("D");
		}else  {
			System.out.println("F");
		} 


		
		
	}
}
